    private void setDate() {
        Date date = new Date();
        SimpleDateFormat dateFormat
                = new SimpleDateFormat("yyyy-MM-dd");
        String formatedDate = dateFormat.format(date);
        dateText.setText(formatedDate);
    }

    private void init() throws ClassNotFoundException, SQLException {
        setDate();
        String newId = IdGenerator.getNewId("Orders", "id", "D");
        invoiceNoText.setText(newId);
        ArrayList<Customer> allCustomer = CustomerController.getAllCustomer();
        for (Customer customer : allCustomer) {
            idCombo.addItem(customer.getId());
        }
        ArrayList<Item> allItems = ItemController.getAllItems();
        for (Item item : allItems) {
            codeCombo.addItem(item.getCode());
        }
    }

    private boolean isValidQty(int qtyOnHand, int qty) {
        if (qty > qtyOnHand) {
            JOptionPane.showMessageDialog(this, "Input is greater than available qty");
            return false;
        } else if (qty < 0) {
            JOptionPane.showMessageDialog(this, "Invalid qty");
            return false;
        }
        return true;
    }
