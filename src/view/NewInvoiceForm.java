    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        if (! qtyText.getText().equals("")) {
            model = (DefaultTableModel) itemTable.getModel();
            boolean exist = false;
            int row = -1;
            for (int i = 0; i < itemTable.getRowCount(); i++) {
                if (itemTable.getValueAt(i, 0).equals(codeCombo.getSelectedItem())) {
                    exist = true;
                    row = i;
                    break;
                }
            }
            
            if (!exist) {
                boolean validQty = isValidQty(Integer.parseInt(qtyOnHandText.getText()), Integer.parseInt(qtyText.getText()));
                if (validQty) {
                    double amount = Double.parseDouble(unitPriceText.getText()) * Integer.parseInt(qtyText.getText());
                    Object[] rowData = {codeCombo.getSelectedItem().toString(), descriptionText.getText(), unitPriceText.getText(), qtyText.getText(), amount};
                    model.addRow(rowData);
                    double totalAmount = Double.parseDouble(totalAmontText.getText());
                    totalAmount += amount;
                    totalAmontText.setText(Double.toString(totalAmount));
                }
            } else {
                int res = MyOptionPane.showOptionDialog(this);
                if (res == MyOptionPane.CHANGE_OPTION) {
                    int qty = Integer.parseInt(itemTable.getValueAt(row, 3).toString());
                    int newQty = Integer.parseInt(qtyText.getText());
                    double amount = qty * Double.parseDouble(unitPriceText.getText());
                    double totalAmount = Double.parseDouble(totalAmontText.getText());
                    totalAmount -= amount;
                    newQty += qty;  //newQty = newQty + qty;
                    boolean validQty = isValidQty(Integer.parseInt(qtyOnHandText.getText()), newQty);
                    if (validQty) {
                        amount = newQty * Double.parseDouble(unitPriceText.getText());
                        totalAmount += amount;
                        itemTable.setValueAt(newQty, row, 3);
                        itemTable.setValueAt(amount, row, 4);
                    }
                    totalAmontText.setText(Double.toString(totalAmount));
                } else if (res == MyOptionPane.REPLACE_OPTION) {
                    boolean validQty = isValidQty(Integer.parseInt(qtyOnHandText.getText()), Integer.parseInt(qtyText.getText()));
                    if (validQty) {
                        int newQty = Integer.parseInt(qtyText.getText());
                        double amount = Double.parseDouble(itemTable.getValueAt(row, 4).toString());
                        double totalAmount = Double.parseDouble(totalAmontText.getText());
                        totalAmount -= amount;
                        amount = newQty * Double.parseDouble(unitPriceText.getText());
                        totalAmount += amount;
                        itemTable.setValueAt(newQty, row, 3);
                        itemTable.setValueAt(amount, row, 4);
                        totalAmontText.setText(Double.toString(totalAmount));
                    }
                }
            }
            qtyText.setText("");
            codeCombo.setSelectedIndex(0);
            codeCombo.requestFocus();
        } else {
            JOptionPane.showMessageDialog(this, "Please input a qty ...");
        }
    }//GEN-LAST:event_addButtonActionPerformed

    private void removeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeButtonActionPerformed
        if (itemTable.getSelectedRow() != -1) {
            String value = itemTable.getValueAt(itemTable.getSelectedRow(), 4).toString();
            double amount = Double.parseDouble(value);
            double totalAmount = Double.parseDouble(totalAmontText.getText());
            totalAmount -= amount;
            totalAmontText.setText(Double.toString(totalAmount));
            model.removeRow(itemTable.getSelectedRow());
        } else {
            JOptionPane.showMessageDialog(this, "please select a row ...");
        }
    }//GEN-LAST:event_removeButtonActionPerformed

    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        Order order = new Order(invoiceNoText.getText(), dateText.getText(), idCombo.getSelectedItem().toString());
        ArrayList<OrderDetail> orderDetailList = new ArrayList<>();
        for (int i = 0; i < itemTable.getRowCount(); i++) {
            OrderDetail orderDetail = new OrderDetail(invoiceNoText.getText(), itemTable.getValueAt(i, 0).toString(), Integer.parseInt(itemTable.getValueAt(i, 3).toString()), Double.parseDouble(itemTable.getValueAt(i, 2).toString()));
            orderDetailList.add(orderDetail);
        }
        try {
            boolean addOrder = OrderController.addOrder(order, orderDetailList);
            if (addOrder) {
                JOptionPane.showMessageDialog(this, "Order saved successfully ...");
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Order saved fail ...");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(NewInvoiceForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveButtonActionPerformed

